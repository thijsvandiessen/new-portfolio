		<p>One of the other fonts I had chosen, DIN 1451 Mittelschrift, has a very special place in my heart. Because it used to be the standard font for road information and traffic signs in Germany and other European countries, you can see some industrialisation and roadworks in it. I believe that this history gives meaning to letters and designs, because people recognise the font and consciously or subconsciously associate it with its previous function. The roman fonts, for example, are very old and are used widely by the church and in official documents. Now we see those letters as if they were set in stone, as they used to be.</p>

		<p>Futura is an example of a very elegant typeface to me. It is not very legible and not very pretty in its heavy form, but very elegant in its lighter forms. I like that the letter ‘j’ is a straight line with a round point on top. This sort of minimalism and stylisation makes Futura very clean and modern.</p>

		<p>I have a lot more to say and a lot more to learn about a lot of typefaces, but these three are special to me.</p>


		<p>Ever since I was a little boy, I loved music. During my student life I performed in different orchestras, played many pieces and performed in many concerts. Because of this passion of mine and the experience I gained during my studies, I got drawn to the marketing of those concerts. In the youth orchestra the idea emerged of making a different poster every year, made by one or two members of the orchestra. In my student orchestra this was already mandatory, and a whole committee worked together to design the banners, posters, program books and the online marketing involved with the promotion of the orchestra’s concerts.</p>

		<p>At my internship at the production company Simpel Media (now part of Endemol) I learned to visualise and devise new tv-formats. I got to know a lot of techniques to visualise ideas. Before, I used Photoshop and Illustrator to do this, but now I also learned to use Adobe After Effects to create special effects and create more depth in visualisations.</p>

		<h1>Workflow</h1>
		<p>For me, the most important step in the proces of designing a poster for a concert is generating an idea that is communicating the essence of the concert while at the same time being easy to make pretty. Usually I know well enough what the music is about, but sometimes talking with someone about the interpretation of the music helps in generating ideas for the poster design.</p>

		<p>Before visualising my ideas in Photoshop, I usually make little sketches in which I design different compositions and explore where lines and focus points are placed. The process of choosing the right font is sometimes also included in this brainstorming process. When having a rough idea of the final outcome, I’m going to look for the images I need. Sometimes this is very easy, but when the idea is a quite unique or specific, you can’t always find the right images fast. The key of designing a good poster is in my opinion just trying out many different combinations with these images, sticking with the good ideas, but not setting anything in stone. In this phase you can easily achieve different effects, and thus trying out different ideas helps creating something great. Keeping things organised in photoshop and working in properly structured layers, masks and folders in this stage is important for me, because this allows you to trace back your design decisions and easily make changes in order to finally compose your definitive design.</p>

		<p>The last step for me is always to color correct my file for the medium I am designing for. Sometimes some lighting effects need to be adjusted for the particular medium, and for print you have to make sure that your CMYK colours pop. Furthermore, at my graduation internship at the company Bitcare, I learned the importance of reducing the file size of the files of my designs. For web or mobile applications you need to be on the sweet spot that balances an excellent image quality and a very small file size. This way, you can e-mail your originally gigabite files without losing the quality you designed for.</p>

		<h1>Example</h1>

		<p>For one programme of a student orchestra that was to take place during the summer, I had an idea to make someting jazzy and summery. I started with a simple quote and some summercolors and images. I used a quote I came up with myself,'The jazz of the sea is in the swing', which was a huge inspiration for the poster.</p>

		<img class="img-fluid" src="img/featured_exercise.jpg" alt="Poster exercise">

		<p>With these elements I formed the design for the background for the final poster that you can see above. I started with the letters of the quote in illustrator, found a cool font and played around with te position and the size of the letters. Next, I looked for some images that I could use, and for some curved swing brushes. I tried to combine these things using different approaches and compositions, each time choosing the one I liked best. In the end I blended images of splashes of ink, of a wall with a crack in it, and of a wave with these brushes and text, to come to this visualisation that in my eyes captured the feeling of the music perfectly.</p>


			<h1>Graduated</h1>
		</div>

		<p>For my graduation project I was asked to design a tool to persuade parents to choose for a childcare that had a digital parent communication tool. My first idea was to make something like an AirBnB for daycare centres, but soon I learned that this was not a solution for todays society. It is already hard to trust somebody with your house, but it's much harder to trust a stranger with your child.</p>

		<p>After interviewing parents, I found out that parents like to communicate their experiences with their friends and family. Not with everybody, but definitely with their closest friends. The way parents share those memories is very diverse. It did however show that parents share this information to stay in contact with their friends and family. After all, nurturing a baby is hard and time-consuming, and for years the most important task of you and the child’s life.</p>

		<p>Choosing a childcare is not only a rational decision for most parents. For an important part this decision is also very emotionally driven. Out of all my stakeholder interviews I learned that open communication between parents and childcare is very appreciated. Parents like to be with their little loved one(s) and are very protective and careful with their baby. Knowing what and when the baby does something is not just nice to know, but very informative for parents in many ways.</p>

		<p>I successfully graduated at The Hague University of applied sciences with this graduation project. With this project I learned that any design decision can have a big impact but also that testing and validating your decisions is crucial for selling your design. This project is only a idea that has the potential to change a business model, now it needs to be carefully implemented without scaring existing customers away.</p>

